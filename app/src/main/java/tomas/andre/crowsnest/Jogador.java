package tomas.andre.crowsnest;

public class Jogador {

    public double posX;
    public double posY;

    public double getPosX() {
        return posX;
    }

    public double getPosY() {
        return posY;
    }

    public Jogador(double posX, double posY) {
        this.posX = posX;
        this.posY = posY;
    }
}
